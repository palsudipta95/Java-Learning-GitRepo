public class MathFunctions {
    public static void main(String[] args) {
        //Ceil
        System.out.println(Math.ceil(3.14159));
        //Floor
        System.out.println(Math.floor(3.14159));
        //Maximum
        System.out.println(Math.max(18, 9));
        //Minimum
        System.out.println(Math.min(18, 9));
        //Absolute
        System.out.println(Math.abs(-24));
        //Square Root
        System.out.println(Math.sqrt(7392));
        //Random Number
        System.out.println(Math.random());
        //Log10
        System.out.println(Math.log10(10));
        //Log
        System.out.println(Math.log(10));
    }
}